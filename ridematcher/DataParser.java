package com.example.appmodel.ridematcher;

import android.util.Log;

import com.google.android.gms.maps.model.LatLng;

import org.json.JSONArray;
import org.json.JSONObject;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;



public class DataParser {

    String distance;
    JSONArray routes;

   public List<LatLng> parse(JSONObject object){
       try {
           routes = object.getJSONArray("routes");
           JSONObject overviewPoly = ((JSONObject)routes.get(0)).getJSONObject("overview_polyline");
           JSONObject distanceObj = routes.getJSONObject(0).getJSONArray("legs").getJSONObject(0).getJSONObject("distance");
           distance = distanceObj.getString("text");
           return decodePolyline(overviewPoly.getString("points"));
       }catch (Exception e){
           e.printStackTrace();
       }
       return null;
   }

    /**
     * Method to decode polyline points
     * Courtesy : http://jeffreysambells.com/2010/05/27/decoding-polylines-from-google-maps-direction-api-with-java
     * */
    private List<LatLng> decodePolyline(String encoded) {
        List<LatLng> poly = new ArrayList<>();
        int index = 0, len = encoded.length();
        int lat = 0, lng = 0;
        while (index < len) {
            int b, shift = 0, result = 0;
            do {
                b = encoded.charAt(index++) - 63;
                result |= (b & 0x1f) << shift;
                shift += 5;
            } while (b >= 0x20);
            int dlat = ((result & 1) != 0 ? ~(result >> 1) : (result >> 1));
            lat += dlat;
            shift = 0;
            result = 0;
            do {
                b = encoded.charAt(index++) - 63;
                result |= (b & 0x1f) << shift;
                shift += 5;
            } while (b >= 0x20);
            int dlng = ((result & 1) != 0 ? ~(result >> 1) : (result >> 1));
            lng += dlng;
            LatLng p = new LatLng((((double) lat / 1E5)),
                    (((double) lng / 1E5)));
            poly.add(p);
        }
        return poly;
    }
}
